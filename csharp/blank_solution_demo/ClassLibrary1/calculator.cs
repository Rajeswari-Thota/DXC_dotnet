namespace ClassLibrary1
{
    public class calculator
    {
        
        public int Sum(int n1,int n2)
        {
            return n1 + n2;
        }
        public int Sub(int n1, int n2)
        {
            return n1 - n2;
        }
        public int Mul(int n1, int n2)
        {
            return n1 * n2;
        }
        public int Div(int n1, int n2)
        {
            return n1 / n2;
        }
        public int Mod(int n1, int n2)
        {
            return n1 % n2;
        }
    }

}
        
        
    

